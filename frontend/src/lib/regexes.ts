/** user_id向けの正規表現
 *  半角英小文字大文字数字および半角アンダーバーいずれかを含む
 *  4文字以上、15文字以下
 */
export const userIdRegex: RegExp = /^[a-zA-Z0-9_]{4,15}$/;

/** password向けの正規表現
 *  半角英小文字大文字数字をそれぞれ1種類以上含む
 *  8文字以上、100文字以下
 */
export const passwordRegex: RegExp =
  /^(?=.*?[a-z])(?=.*?[A-Z])(?=.*?\d)(?=.*?[!@#$%^&*()_+\-=\[\]{};':"\\|,.<>\/?])[a-zA-Z\d!@#$%^&*()_+\-=\[\]{};':"\\|,.<>\/?]{8,100}$/;
